

@SuppressWarnings("serial")
public class Power_Exception extends Exception {

	public Power_Exception() {
		super();
		
	}

	public Power_Exception(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
		
	}

	public Power_Exception(String message, Throwable cause) {
		super(message, cause);
		
	}

	public Power_Exception(String message) {
		super(message);
		
	}

	public Power_Exception(Throwable cause) {
		super(cause);
		
	}

	
}
